package captube.org.captube.custom;

public class CaptubeInfo {
    String title;
    CaptubeImage[] captubeImages;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public CaptubeImage[] getCaptubeImages() {
        return captubeImages;
    }

    public void setCaptubeImages(CaptubeImage[] captubeImages) {
        this.captubeImages = captubeImages;
    }
}
